@st.cache_data(ttl=300)
def fetch_yahoo_data(ticker: str, period="6mo", interval="1d", max_retries=3) -> pd.DataFrame:
    """
    Fetch historical market data from Yahoo Finance with retry logic and error handling.
    
    Args:
        ticker: Stock symbol to fetch data for
        period: Time period to fetch (1d, 5d, 1mo, 3mo, 6mo, 1y, 2y, 5y, 10y, ytd, max)
        interval: Data interval (1m, 2m, 5m, 15m, 30m, 60m, 90m, 1h, 1d, 5d, 1wk, 1mo, 3mo)
        max_retries: Maximum number of retry attempts
    
    Returns:
        pd.DataFrame: DataFrame containing the historical data or empty DataFrame on failure
    """
    import time
    from requests.exceptions import RequestException
    
    # Clean the ticker symbol (in case it's in the "SYMBOL — Description" format)
    m = re.match(r"^([A-Za-z0-9\-\._=]+)", ticker or "")
    clean_ticker = m.group(1) if m else ticker
    
    if not clean_ticker:
        st.error("No ticker symbol provided")
        return pd.DataFrame()
    
    # Set user agent to prevent rate limiting
    # Note: pdr_override() is not available in current yfinance versions
    
    for attempt in range(max_retries):
        try:
            # Create Ticker object
            t = yf.Ticker(clean_ticker)
            
            # Fetch data with timeout
            df = t.history(period=period, interval=interval)
            
            # Validate data
            if df is None or df.empty:
                if attempt < max_retries - 1:
                    st.warning(f"Attempt {attempt + 1} failed for {clean_ticker}. Retrying...")
                    time.sleep(2 ** attempt)  # Exponential backoff
                    continue
                st.error(f"❌ No data returned for {clean_ticker} after {max_retries} attempts.")
                st.info(" **Troubleshooting tips:**")
                st.info("• Check if the ticker symbol is correct (e.g., AAPL, TSLA, BTC-USD)")
                st.info("• Try different ticker formats (e.g., .TO for Canadian stocks)")
                st.info("• Verify the ticker exists on Yahoo Finance")
                st.info("• Check your internet connection")
                return pd.DataFrame()
            
            # Reset index and clean up
            df = df.reset_index()
            
            # Handle date column
            if 'Date' in df.columns:
                df['Date'] = pd.to_datetime(df['Date'])
                try:
                    if df['Date'].dt.tz is not None:
                        df['Date'] = df['Date'].dt.tz_convert(None)
                except Exception:
                    try:
                        df['Date'] = df['Date'].dt.tz_localize(None)
                    except Exception:
                        pass
            
            # Convert numeric columns
            numeric_cols = ['Open', 'High', 'Low', 'Close', 'Volume', 'Dividends', 'Stock Splits']
            for col in numeric_cols:
                if col in df.columns:
                    df[col] = pd.to_numeric(df[col], errors='coerce')
            
            # Ensure we have required columns
            required_cols = ['Date', 'Close']
            if not all(col in df.columns for col in required_cols):
                if attempt < max_retries - 1:
                    time.sleep(2 ** attempt)
                    continue
                st.error(f"Incomplete data received for {clean_ticker}")
                return pd.DataFrame()
            
            # Clean and return data
            df = df.dropna(subset=['Date', 'Close']).reset_index(drop=True)
            
            if df.empty:
                st.warning(f"No valid data points for {clean_ticker} after cleaning")
                return pd.DataFrame()
                
            return df
            
        except RequestException as e:
            if attempt == max_retries - 1:
                st.error(f"🌐 Network error while fetching data for {clean_ticker}")
                st.error(f"Error details: {str(e)}")
                st.info(" **Network troubleshooting:**")
                st.info("• Check your internet connection")
                st.info("• Try again in a few moments")
                st.info("• The Yahoo Finance API might be temporarily unavailable")
                return pd.DataFrame()
            st.warning(f"Network error on attempt {attempt + 1}. Retrying...")
            time.sleep(2 ** attempt)
            
        except Exception as e:
            if attempt == max_retries - 1:
                st.error(f"❌ Unexpected error while fetching data for {clean_ticker}")
                st.error(f"Error details: {str(e)}")
                st.info(" **Please try:**")
                st.info("• Verifying the ticker symbol is correct")
                st.info("• Using a different ticker format")
                st.info("• Refreshing the page and trying again")
                return pd.DataFrame()
            st.warning(f"Error on attempt {attempt + 1}. Retrying...")
            time.sleep(2 ** attempt)
    
    return pd.DataFrame()

def compute_indicators(df: pd.DataFrame) -> pd.DataFrame:
    """Compute comprehensive technical indicators with proper data leakage prevention"""
    df = df.copy().reset_index(drop=True)
    
    # Ensure we have enough data for calculations
    if len(df) < 50:
        # For small datasets, use shorter periods
        ma_period = min(20, len(df) // 2)
        bb_period = min(20, len(df) // 2)
        rsi_period = min(14, len(df) // 2)
    else:
        ma_period = 20
        bb_period = 20
        rsi_period = 14
    
    # Moving Averages - using proper naming convention
    df['MA20'] = df['Close'].rolling(window=ma_period, min_periods=1).mean()
    df['MA50'] = df['Close'].rolling(window=min(50, len(df)), min_periods=1).mean()
    df['SMA_20'] = df['MA20']  # Alias for technical analysis compatibility
    df['SMA_50'] = df['MA50']  # Alias for technical analysis compatibility
    
    # Exponential Moving Averages
    df['EMA12'] = df['Close'].ewm(span=12, adjust=False).mean()
    df['EMA26'] = df['Close'].ewm(span=26, adjust=False).mean()
    
    # MACD and Signal Line
    df['MACD'] = df['EMA12'] - df['EMA26']
    df['MACD_Signal'] = df['MACD'].ewm(span=9, adjust=False).mean()
    df['MACD_Hist'] = df['MACD'] - df['MACD_Signal']
    
    # Bollinger Bands
    df['BB_Middle'] = df['Close'].rolling(window=bb_period, min_periods=1).mean()
    df['BB_Std'] = df['Close'].rolling(window=bb_period, min_periods=1).std(ddof=0)
    df['BB_Upper'] = df['BB_Middle'] + (2 * df['BB_Std'])
    df['BB_Lower'] = df['BB_Middle'] - (2 * df['BB_Std'])
    
    # Legacy naming for compatibility
    df['BB_MID'] = df['BB_Middle']
    df['BB_STD'] = df['BB_Std']
    df['BB_UP'] = df['BB_Upper']
    df['BB_LOW'] = df['BB_Lower']
    
    # RSI Calculation with proper handling
    delta = df['Close'].diff()
    up = delta.clip(lower=0)
    down = -delta.clip(upper=0)
    
    # Use exponential moving average for RSI smoothing
    roll_up = up.ewm(span=rsi_period, adjust=False).mean()
    roll_down = down.ewm(span=rsi_period, adjust=False).mean()
    
    rs = roll_up / (roll_down + 1e-8)  # Avoid division by zero
    df['RSI'] = 100 - (100 / (1 + rs))
    df['RSI'] = df['RSI'].clip(0, 100).fillna(50)
    
    # Additional indicators for better forecasting
    # Stochastic Oscillator
    low_min = df['Low'].rolling(window=14, min_periods=1).min()
    high_max = df['High'].rolling(window=14, min_periods=1).max()
    df['Stoch_K'] = 100 * ((df['Close'] - low_min) / (high_max - low_min + 1e-8))
    df['Stoch_D'] = df['Stoch_K'].rolling(window=3, min_periods=1).mean()
    
    # Williams %R
    df['Williams_R'] = -100 * ((high_max - df['Close']) / (high_max - low_min + 1e-8))
    
    # Commodity Channel Index (CCI)
    typical_price = (df['High'] + df['Low'] + df['Close']) / 3
    sma_tp = typical_price.rolling(window=20, min_periods=1).mean()
    mad = typical_price.rolling(window=20, min_periods=1).apply(lambda x: np.mean(np.abs(x - x.mean())))
    df['CCI'] = (typical_price - sma_tp) / (0.015 * mad + 1e-8)
    
    # Average True Range (ATR)
    high_low = df['High'] - df['Low']
    high_close = np.abs(df['High'] - df['Close'].shift())
    low_close = np.abs(df['Low'] - df['Close'].shift())
    true_range = np.maximum(high_low, np.maximum(high_close, low_close))
    df['ATR'] = true_range.rolling(window=14, min_periods=1).mean()
    
    # Price Rate of Change
    df['ROC'] = df['Close'].pct_change(periods=10) * 100
    
    # Volume indicators
    if 'Volume' in df.columns:
        df['Volume_MA'] = df['Volume'].rolling(window=20, min_periods=1).mean()
        df['Volume_Ratio'] = df['Volume'] / (df['Volume_MA'] + 1e-8)
        
        # On-Balance Volume
        df['OBV'] = np.where(df['Close'] > df['Close'].shift(), df['Volume'], 
                            np.where(df['Close'] < df['Close'].shift(), -df['Volume'], 0)).cumsum()
    
    return df


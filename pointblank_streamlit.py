@st.cache_data(ttl=300)
def generate_technical_analysis(df: pd.DataFrame, ticker: str) -> dict:
    """Generate comprehensive technical analysis for a stock"""
    if df.empty or len(df) < 20:
        return {}
    
    analysis = {}
    latest = df.iloc[-1]
    prev = df.iloc[-2] if len(df) > 1 else latest
    
    # Price Analysis
    price_change = latest['Close'] - prev['Close']
    price_change_pct = (price_change / prev['Close']) * 100
    
    analysis['price'] = {
        'current': latest['Close'],
        'change': price_change,
        'change_pct': price_change_pct,
        'trend': 'Bullish' if price_change > 0 else 'Bearish' if price_change < 0 else 'Neutral'
    }
    
    # Moving Averages Analysis
    if ('SMA_20' in df.columns and 'SMA_50' in df.columns) or ('MA20' in df.columns and 'MA50' in df.columns):
        sma_20 = latest.get('SMA_20', latest.get('MA20', 0))
        sma_50 = latest.get('SMA_50', latest.get('MA50', 0))
        current_price = latest['Close']
        
        if current_price > sma_20 > sma_50:
            ma_signal = "Strong Bullish"
        elif current_price > sma_20 and sma_20 < sma_50:
            ma_signal = "Mixed"
        elif current_price < sma_20 < sma_50:
            ma_signal = "Strong Bearish"
        else:
            ma_signal = "Neutral"
            
        analysis['moving_averages'] = {
            'sma_20': sma_20,
            'sma_50': sma_50,
            'signal': ma_signal
        }
    
    # RSI Analysis
    if 'RSI' in df.columns:
        rsi = latest['RSI']
        if rsi > 70:
            rsi_signal = "Overbought"
        elif rsi < 30:
            rsi_signal = "Oversold"
        else:
            rsi_signal = "Neutral"
            
        analysis['rsi'] = {
            'value': rsi,
            'signal': rsi_signal
        }
    
    # MACD Analysis
    if 'MACD' in df.columns and 'MACD_Signal' in df.columns:
        macd = latest['MACD']
        macd_signal = latest['MACD_Signal']
        macd_hist = latest.get('MACD_Hist', macd - macd_signal)
        
        if macd > macd_signal and macd_hist > 0:
            macd_trend = "Bullish"
        elif macd < macd_signal and macd_hist < 0:
            macd_trend = "Bearish"
        else:
            macd_trend = "Neutral"
            
        analysis['macd'] = {
            'macd': macd,
            'signal': macd_signal,
            'histogram': macd_hist,
            'trend': macd_trend
        }
    
    # Bollinger Bands Analysis
    if ('BB_Upper' in df.columns and 'BB_Lower' in df.columns) or ('BB_UP' in df.columns and 'BB_LOW' in df.columns):
        bb_upper = latest.get('BB_Upper', latest.get('BB_UP', 0))
        bb_lower = latest.get('BB_Lower', latest.get('BB_LOW', 0))
        bb_middle = latest.get('BB_Middle', latest.get('BB_MID', (bb_upper + bb_lower) / 2))
        current_price = latest['Close']
        
        if current_price > bb_upper:
            bb_signal = "Overbought"
        elif current_price < bb_lower:
            bb_signal = "Oversold"
        else:
            bb_signal = "Normal"
            
        analysis['bollinger_bands'] = {
            'upper': bb_upper,
            'lower': bb_lower,
            'middle': bb_middle,
            'signal': bb_signal
        }
    
    # Volume Analysis
    if 'Volume' in df.columns:
        avg_volume = df['Volume'].tail(20).mean()
        current_volume = latest['Volume']
        volume_ratio = current_volume / avg_volume if avg_volume > 0 else 1
        
        if volume_ratio > 1.5:
            volume_signal = "High Volume"
        elif volume_ratio < 0.5:
            volume_signal = "Low Volume"
        else:
            volume_signal = "Normal Volume"
            
        analysis['volume'] = {
            'current': current_volume,
            'average': avg_volume,
            'ratio': volume_ratio,
            'signal': volume_signal
        }
    
    # Overall Technical Score
    bullish_signals = 0
    total_signals = 0
    
    if 'moving_averages' in analysis:
        total_signals += 1
        if 'Strong Bullish' in analysis['moving_averages']['signal']:
            bullish_signals += 1
        elif 'Mixed' in analysis['moving_averages']['signal']:
            bullish_signals += 0.5
    
    if 'rsi' in analysis:
        total_signals += 1
        if analysis['rsi']['signal'] == 'Oversold':
            bullish_signals += 1
        elif analysis['rsi']['signal'] == 'Overbought':
            bullish_signals += 0
    
    if 'macd' in analysis:
        total_signals += 1
        if analysis['macd']['trend'] == 'Bullish':
            bullish_signals += 1
        elif analysis['macd']['trend'] == 'Neutral':
            bullish_signals += 0.5
    
    if 'bollinger_bands' in analysis:
        total_signals += 1
        if analysis['bollinger_bands']['signal'] == 'Oversold':
            bullish_signals += 1
        elif analysis['bollinger_bands']['signal'] == 'Overbought':
            bullish_signals += 0
    
    if total_signals > 0:
        technical_score = (bullish_signals / total_signals) * 100
        if technical_score >= 70:
            overall_signal = "Strong Buy"
        elif technical_score >= 60:
            overall_signal = "Buy"
        elif technical_score >= 40:
            overall_signal = "Hold"
        elif technical_score >= 30:
            overall_signal = "Sell"
        else:
            overall_signal = "Strong Sell"
    else:
        technical_score = 50
        overall_signal = "Hold"
    
    analysis['overall'] = {
        'score': technical_score,
        'signal': overall_signal
    }
    
    return analysis

@st.cache_data(ttl=3600)
def validate_ticker(ticker: str) -> dict:
    """Validate if a ticker symbol exists on Yahoo Finance"""
    if not ticker:
        return {"valid": False, "error": "No ticker provided"}
    
    try:
        # Clean the ticker symbol
        m = re.match(r"^([A-Za-z0-9\-\._=]+)", ticker)
        clean_ticker = m.group(1) if m else ticker
        
        # Try to get basic info
        t = yf.Ticker(clean_ticker)
        info = t.info
        
        # Check if we got meaningful data
        if not info or len(info) < 5:
            return {"valid": False, "error": "Ticker not found or no data available"}
        
        # Check for required fields
        required_fields = ['symbol', 'shortName', 'longName']
        has_required = any(field in info and info[field] for field in required_fields)
        
        if not has_required:
            return {"valid": False, "error": "Invalid ticker - missing basic information"}
        
        return {
            "valid": True, 
            "symbol": clean_ticker,
            "name": info.get('shortName') or info.get('longName', clean_ticker),
            "exchange": info.get('exchange', 'Unknown'),
            "currency": info.get('currency', 'USD'),
            "market_cap": info.get('marketCap'),
            "sector": info.get('sector'),
            "industry": info.get('industry')
        }
        
    except Exception as e:
        return {"valid": False, "error": f"Error validating ticker: {str(e)}"}

